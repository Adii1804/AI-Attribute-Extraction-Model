const extractSection = (lines: string[], header: string, nextHeader: string | null) => {
  const start = lines.findIndex(line => line.toLowerCase() === header.toLowerCase());
  if (start === -1) return [];
  const startIndex = start + 1;
  const endIndex = nextHeader
    ? lines.findIndex((line, idx) => idx > start && line.toLowerCase() === nextHeader.toLowerCase())
    : -1;
  return lines.slice(startIndex, endIndex === -1 ? undefined : endIndex);
};

const rawLines = RAW_MAJOR_CATEGORY_DATA.split(/\r?\n/).map(line => line.trim()).filter(Boolean);
const fullForms = extractSection(rawLines, 'FULL FORM MAJOR CATEGORY', 'SHORT FORM MAJOR CATEGORY');
const shortForms = extractSection(rawLines, 'SHORT FORM MAJOR CATEGORY', null);
const pairCount = Math.min(fullForms.length, shortForms.length);

const paired = Array.from({ length: pairCount }, (_, index) => ({
  fullForm: fullForms[index],
  shortForm: shortForms[index]
}));

const uniqueByShortForm = new Map<string, { shortForm: string; fullForm: string }>();
for (const pair of paired) {
  if (!pair.shortForm) continue;
  if (!uniqueByShortForm.has(pair.shortForm)) {
    uniqueByShortForm.set(pair.shortForm, pair);
  }
}

export const MAJOR_CATEGORY_ALLOWED_VALUES = Array.from(uniqueByShortForm.values());
